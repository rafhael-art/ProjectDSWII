package com.cibertec.edu.pe.service;

import com.cibertec.edu.pe.interfaces.ISeccionService;
import com.cibertec.edu.pe.modelo.Rol;
import com.cibertec.edu.pe.modelo.Seccion;
import com.cibertec.edu.pe.repository.IRolRepository;
import com.cibertec.edu.pe.repository.ISeccionRepository;
import java.util.List;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;

/**
 *
 * @author rafhael
 */
@Service
public class SecccionService implements ISeccionService{
    
    @Autowired
    private ISeccionRepository _repository;
    
    @Override
    public List<Seccion> findAll() throws Exception {
        try {
            return _repository.findAll();
        } catch (Exception e) {
            throw  new Exception(e.getMessage());
        }
    }

    @Override
    public Seccion findById(Long id) throws Exception {
        try {
            return _repository.findById(id).get();
        } catch (Exception e) {
            throw  new Exception(e.getMessage());
        }
    }

    @Override    
    public Seccion save(Seccion entity) throws Exception {
        try {
            return _repository.save(entity);
        } catch (Exception e) {
            throw  new Exception(e.getMessage());
        }
    }

    @Override
    public Seccion update(Seccion entity) throws Exception {
        try {
            return _repository.save(entity);
        } catch (Exception e) {
            throw  new Exception(e.getMessage());
        }
    }

    @Override
    public Boolean delete(Long id) throws Exception {
        try {
            _repository.deleteById(id);
            return true;
        } catch (Exception e) {
            throw  new Exception(e.getMessage());
        }
    }
}
